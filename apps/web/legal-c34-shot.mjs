import { chromium } from '@playwright/test';
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1280, height: 1000 } });
await page.goto('http://localhost:3060/login');
await page.fill('#email', 'invitee@test.local');
await page.fill('#password', 'secret123');
await page.getByRole('button', { name: 'Войти' }).click();
await page.waitForURL('**/dashboard**');
await page.goto('http://localhost:3060/dashboard/profile');
await page.waitForSelector('text=Мои согласия');
await page.waitForSelector('text=Договор-оферта');
const card = page.locator('[data-slot="card"]', { has: page.locator('text=Мои согласия') });
await card.screenshot({ path: '/tmp/shots/my-consents-card.png' });
await browser.close();
