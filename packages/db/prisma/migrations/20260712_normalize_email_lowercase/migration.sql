-- Нормализация email в нижний регистр (эпик «Вход не работает из-за регистра email»).
--
-- ПРОБЛЕМА: email в "User" хранится регистрозависимо. Часть пользователей
-- зарегистрировалась с планшета, где первая буква подставляется заглавной
-- (`Marina@mail.ru`), и теперь не может войти — вход ищет по точному совпадению.
-- Нормализация на входе (normalizeEmail в lib/validation) сделана в этом же
-- изменении в роутах auth/users/streams-public. Эта миграция приводит к нижнему
-- регистру УЖЕ существующие записи и закрепляет инвариант на уровне БД.
--
-- =====================================================================================
-- !!! ПЕРЕД ДЕПЛОЕМ ВЫПОЛНИТЬ ВРУЧНУЮ НА ПРОДЕ — ПРОВЕРКА КОЛЛИЗИЙ !!!
-- =====================================================================================
-- После lower(email) два аккаунта вида `Foo@x.ru` и `foo@x.ru` дадут одинаковый email
-- и нарушат уникальность — тогда И Шаг 1 (через "User_email_key"), И Шаг 2 (создание
-- функционального индекса) УПАДУТ, а вместе с ними `prisma migrate deploy` в start.sh,
-- что заблокирует авто-деплой на VPS. Поэтому СНАЧАЛА выявляем дубли запросом:
--
--   SELECT lower(btrim(email)) AS norm, count(*) AS cnt, array_agg(id) AS ids, array_agg(email) AS emails
--   FROM "User"
--   GROUP BY lower(btrim(email))
--   HAVING count(*) > 1;
--
-- Если запрос вернул строки — дубли РАЗРУЛИТЬ ВРУЧНУЮ ДО деплоя (автоматически сливать
-- аккаунты НЕЛЬЗЯ — за ними разные данные/прогресс/баланс):
--   1) Определить, какой аккаунт основной (по активности, балансу balanceKopecks,
--      прогрессу, последнему входу) — его email оставляем и приводим к lower().
--   2) Второй (дубль) — переименовать email на не конфликтующий (например, добавить
--      суффикс: `foo+dup-<id>@x.ru`) ЛИБО деактивировать (isActive = false). НЕ удалять
--      без согласования: за ним могут быть платежи/начисления.
--   3) Связаться с владельцем дубля, если аккаунт реальный.
-- Только после устранения коллизий (повторный запрос выше вернул 0 строк) — деплоить.
--
-- АДДИТИВНОСТЬ: миграция НЕ меняет структуру таблицы (нет ADD/DROP COLUMN, нет смены
-- типа). Шаг 1 — backfill данных (идемпотентный: повторный прогон ничего не меняет,
-- т.к. условие email <> lower(email) уже ложно). Шаг 2 — добавление индекса. Локи
-- короткие (таблица "User" небольшая), CONCURRENTLY не используем намеренно — его
-- нельзя выполнять внутри транзакции `prisma migrate deploy`.

-- Шаг 1. Бэкофилл: приводим все email к нижнему регистру и срезаем краевые пробелы
-- (btrim — зеркало trim() в normalizeEmail на стороне приложения: исторические записи
-- с пробелами по краям иначе остались бы ненаходимыми). WHERE отсекает уже
-- нормализованные строки, чтобы не дёргать триггеры/updatedAt без нужды.
UPDATE "User"
SET "email" = lower(btrim("email"))
WHERE "email" <> lower(btrim("email"));

-- Шаг 2. БД-инвариант «пояс плюс подтяжки»: уникальность email регистронезависима
-- НАВСЕГДА. Функциональный уникальный индекс по lower(email) — сырым SQL, т.к. Prisma
-- не поддерживает функциональные индексы в schema.prisma (поле остаётся `email @unique`,
-- штатный "User_email_key" сохраняется как защита от точных дублей). Даже если в коде
-- где-то забудут нормализовать вход — БД не даст создать `Foo@x.ru` при наличии
-- `foo@x.ru`. После Шага 1 коллизий быть не должно (их разрулили вручную выше).
CREATE UNIQUE INDEX "User_email_lower_key" ON "User" (lower("email"));
